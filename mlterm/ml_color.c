/* --- global functions --- */

char *
ml_get_color_name(
	ml_color_t  color
	)
{
	if( IS_VTSYS_COLOR(color))
	{
		if( color & ML_BOLD_COLOR_MASK)
		{
			return  color_name_table[color & ~ML_BOLD_COLOR_MASK] ;
		}
		else
		{
			return  color_name_table[color] + 3 ;
		}
	}
	else if( IS_256_COLOR(color))
	{
		/* !Notice! Not reentrant */
		
		snprintf( color_name_256, sizeof( color_name_256), "%d", color) ;

		return  color_name_256 ;
	}
	else
	{
		return  NULL ;
	}
}

ml_color_t
ml_get_color(
	char *  name
	)
{
	ml_color_t  color ;

	for( color = ML_BLACK ; color <= ML_WHITE ; color++)
	{
		if( strcmp( name, color_name_table[color] + 3) == 0)
		{
			return  color ;
		}
		else if( strcmp( name, color_name_table[color]) == 0)
		{
			return  color | ML_BOLD_COLOR_MASK ;
		}
	}

	if( sscanf( name, "%d", (int*) &color) == 1)
	{
		if( IS_256_COLOR(color))
		{
			return  color ;
		}
	}

	return  ML_UNKNOWN_COLOR ;
}

int
ml_get_color_rgb(
	ml_color_t  color,
	u_int8_t *  red,
	u_int8_t *  green,
	u_int8_t *  blue
	)
{
	if( ! IS_VALID_COLOR(color))
	{
		return  0 ;
	}

	if( IS_VTSYS_COLOR(color))
	{
		*red = vtsys_color_rgb_table[ color & ~ML_BOLD_COLOR_MASK ][0] ;
		*green = vtsys_color_rgb_table[ color & ~ML_BOLD_COLOR_MASK ][1] ;
		*blue = vtsys_color_rgb_table[ color & ~ML_BOLD_COLOR_MASK ][2] ;
	}
	else if( color <= 0xe7)
	{
		u_int8_t  tmp ;

		tmp = (color - 0x10) % 6 ;
		*blue = tmp ? (tmp * 40 + 55) & 0xff : 0 ;

		tmp = ((color - 0x10) / 6) % 6 ;
		*green = tmp ? (tmp * 40 + 55) & 0xff : 0 ;

		tmp = ((color - 0x10) / 36) % 6 ;
		*red = tmp ? (tmp * 40 + 55) & 0xff : 0 ;
	}
	else /* if( color >= 0xe8) */
	{
		u_int8_t  tmp ;

		tmp = (color - 0xe8) * 10 + 8 ;

		*blue = tmp ;
		*green = tmp ;
		*red = tmp ;
	}
	
	return 1 ;
}

int
ml_color_parse_rgb_name(
	u_int8_t *  red ,
	u_int8_t *  green ,
	u_int8_t *  blue ,
	char *  name
	)
{
	int  _red ;
	int  _green ;
	int  _blue ;
	size_t  name_len ;

	name_len = strlen( name) ;
	
	if( ( name_len == 7 && sscanf( name, "#%2x%2x%2x" , &_red , &_green , &_blue) == 3) ||
		( name_len == 12 && sscanf( name, "rgb:%2x/%2x/%2x", &_red, &_green, &_blue) == 3))
	{
	#ifdef  __DEBUG
		kik_debug_printf( KIK_DEBUG_TAG " %x %x %x\n" , _red , _green , _blue) ;
	#endif

		*red = _red ;
		*green = _green ;
		*blue = _blue ;

		return  1 ;
	}
	else if( ( name_len == 13 && sscanf( name, "#%4x%4x%4x" , &_red , &_green , &_blue) == 3) ||
		( name_len == 18 && sscanf( name, "rgb:%4x/%4x/%4x", &_red, &_green, &_blue) == 3) ||
		/*
		 * XXX
		 * "RRRR-GGGG-BBBB" length is 14, but 2.4.0 or before accepts
		 * "RRRR-GGGG-BBBB....."(trailing any characters) format and
		 * what is worse "RRRR-GGGG-BBBB;" appears in etc/color sample file.
		 * So, more than 14 length is also accepted for backward compatiblity.
		 */
		( name_len >= 14 && sscanf( name, "%4x-%4x-%4x" , &_red , &_green , &_blue) == 3))
	{
	#ifdef  __DEBUG
		kik_debug_printf( KIK_DEBUG_TAG " %x %x %x\n" , _red , _green , _blue) ;
	#endif
	
		*red = (_red >> 4) & 0xff ;
		*green = (_green >> 4) & 0xff ;
		*blue = (_blue >> 4) & 0xff ;

		return 1 ;
	}
	
	return  0 ;
}

